    public int cut(int n) {
        ArrayList<Integer> list=new ArrayList<Integer>();
        for(int i=2; i<=n/2; i++)
            if(n%i==0) list.add(i);
        int res=1;
        boolean toCut;
        for(int i=1; i<n; i++){
            toCut=false;
            for(int j : list)
                if(i%j==0){
                    toCut=true;
                    break;
                }
            if(toCut) res++;
        }
        return res;


    }
