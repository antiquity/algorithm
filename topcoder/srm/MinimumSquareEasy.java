    int count(int l, int t, int r, int b, int[] x, int[] y){
        int N=y.length;
        int cnt=0;
        for(int i=0; i<N;i++){
            if(x[i]>=l && x[i]<=r && y[i]>=t && y[i]<=b)
                cnt++;
        }
        return cnt;
    }

    public long minArea(int[] x, int[] y) {
        int N=x.length;
        int[] xx=Arrays.copyOf(x,N);
        int[] yy=Arrays.copyOf(y,N);
        Arrays.sort(xx); Arrays.sort(yy);
        long res=Long.MAX_VALUE;
        long area;
        //println(Arrays.toString(x));
        //println(Arrays.toString(y));
        //println(Arrays.toString(xx));
        //println(Arrays.toString(yy));
        long temp;
        for(int i=0;i<N;i++)
            for(int j=0;j<N;j++)
                for(int k=i;k<N;k++)
                    for(int l=j;l<N;l++){
                        if(count(xx[i],yy[j],xx[k],yy[l],x,y)>=N-2){
                            temp=Math.max((2l+xx[k]-xx[i]),(2l+yy[l]-yy[j]));
                            area=temp*temp;
                            if(area<res) res=area;
                        }
                    }

        return res;
    }
