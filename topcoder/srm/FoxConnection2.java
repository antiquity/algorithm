        N=A.length+1;
        g= new int[N+1][N+1];
        for(int i=0; i<=N; i++)
            for(int j=0; j<=N; j++)
                g[i][j]=0;
        for(int i=0; i<A.length; i++){
            g[A[i]][B[i]]=1;
            g[B[i]][A[i]]=1;
        }
        return assign(1,k);
