    public String isPossible(int[] values) {
        int[] dif = new int[values.length-1];
        for(int i=0; i<values.length-1; i++)
            dif[i]=values[i+1]/values[i];
        Arrays.sort(dif);
        for(int i=0; i<dif.length; i++)
            if(dif[i]<=i+1) return "Impossible";
        return "Possible";

    }
