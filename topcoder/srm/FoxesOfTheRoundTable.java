    public int[] minimalDifference(int[] h) {
        int n = h.length;
        int[] idx = new int[n];

        for(int i=0; i<n; i++) idx[i]=i;
        for(int i=1; i<n; i++)
            for(int j=i; j>=1 && h[j]<h[j-1]; j--){
                int temp = h[j-1];
                h[j-1]=h[j];
                h[j]=temp;
                temp=idx[j-1];
                idx[j-1]=idx[j];
                idx[j]=temp;
            }

        int[] ret = new int[n];
        ret[0]=idx[0];
        int i=1, j=n-1;
        while(i<=j){
            if(i*2<=n) ret[i]=idx[i*2-1];
            if((n-j)*2<n) ret[j]=idx[(n-j)*2];
            i++;
            j--;
        }

        return ret;

    }

