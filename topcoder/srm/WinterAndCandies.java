    public int getNumber(int[] type) {
        Arrays.sort(type);
        int N=0,i=0;
        while(i<type.length && type[i]==N+1){
            N++; i++;
            while(i<type.length && type[i]==N) i++;
        }
        int end = i;
        i=0;
        int[] n = new int[N+1];
        Arrays.fill(n,0);
        while(i<end){ n[type[i]]++; i++; }
        int ret=0; end=1;
        for(i=1; i<=N; i++){
            end*=n[i];
            ret+=end;
        }
        return ret;
    }
