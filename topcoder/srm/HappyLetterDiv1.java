    boolean canWin(int l, int[] count){
        int[] cnt = count.clone();
        int c1,c2,l1,l2;
        while(true){
            c1=0; c2=0; l1=-1; l2=-1;
            for(int i=0; i<cnt.length; i++) if(i!=l){
                if(cnt[i]>c1){
                    c2=c1; c1=cnt[i]; l2=l1; l1=i;
                }else if(cnt[i]>c2){
                    c2=cnt[i]; l2=i;
                }
            }
            if(c1>0 && c2>0){
                cnt[l1]--; cnt[l2]--;
            }else if(c1>0)
                return cnt[l]>c1;
            else return cnt[l]>0;
        }
    }
    public String getHappyLetters(String letters) {
        char[] l = letters.toCharArray();
        Arrays.sort(l);
        int[] count = new int[26];
        for(char i : l) count[i-'a']++;
        String ret = "";
        for(int i=0; i<26; i++) if(count[i]>0 && canWin(i,count))
            ret += (char)('a'+i);
        return ret;
    }
