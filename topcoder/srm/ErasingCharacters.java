    public String simulate(String s) {
        boolean cond;
        while(true){
            cond=false;
            for(int i=0; i<s.length()-1; i++){
                if(s.charAt(i)==s.charAt(i+1)){
                    s=s.substring(0,i)+s.substring(i+2,s.length());
                    cond=true;
                    //System.out.println(s);
                    break;
                }
            }
            if(cond) continue;
            else break;
        }
        return s;

    }
