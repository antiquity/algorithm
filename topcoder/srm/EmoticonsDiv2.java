    int solve(int n){
        int temp;
        if(dp[n]!=-1) return dp[n];
        dp[n]=n;
        for(int i=2; i<=n/2; i++){
            if(n%i==0){
                temp=solve(n/i)+i;
                if(temp<dp[n]) dp[n]=temp;
            }
        }
        return dp[n];
    }
    public int printSmiles(int smiles) {
        dp = new int[smiles+1];
        Arrays.fill(dp, -1);
        dp[1]=0;
        return solve(smiles);
    }
