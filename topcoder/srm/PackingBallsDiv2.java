    public int minPacks(int R, int G, int B) {
        int res=0;
        res+=(R/3); R%=3;
        res+=(G/3); G%=3;
        res+=(B/3); B%=3;
        int m = Math.min(Math.min(R,G),B);
        res+=m;
        R-=m; G-=m; B-=m;
        int a = Math.max(Math.max(R,G),B);
        int b = R+G+B-a;
        if(a==0) return res;
        if(a==1) return res+1;
        if(a==2){
            if(b>=1) return res+2;
            else
                return res+1;
        }
        return 0;
    }
