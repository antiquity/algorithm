    public int solve(int[] A) {
        int N = A.length;
        int k = -1;
        for(int j=0; j<p.length; j++)
            for(int i=0; i< N; i++) if(A[i]%p[j]==0){
                primes.add(p[j]);
                break;
            }
        for(int i=0; i< N-k; i++){
            for(int j=N-1; j>i+k; j--) if(isNice(A,i,j) && (j-i>k)){
                    k=j-i;
                    break;
            }
        }
        return k;
    }
    boolean isNice(int[] A, int i, int j){
        int cnt;
        for(int k=0; k<primes.size(); k++){
            cnt=0;
            for(int l=i; l<=j; l++) if(A[l]%primes.get(k)==0)
                cnt++;
            if(2*cnt>(j-i)) return true;
        }
        return false;
    }
