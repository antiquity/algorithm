    public int getNumber(int[] position, int[] count, int time) {
        for(int i=1; i<position.length; i++){
            int j=i-1, p=position[i], c=count[i];
            while(j>=0 && position[j]>p){
                position[j+1]=position[j];
                count[j+1]=count[j];
                j--;
            }
            j++; position[j]=p; count[j]=c;
        }
        // System.out.println(Arrays.toString(position));
        // System.out.println(Arrays.toString(count));

        int ret=0;
        for(int i=0; i<position.length;){
            int j=i, sum=count[i]; boolean crowd=(2*time+1)<count[i];
            while(j<position.length-1 && position[j+1]<=position[i]+2*time){
                j++; sum+=count[j];
                if(2*time+1<count[j]) crowd=true;
                if(j-i+1+2*time < sum) crowd=true;
            }
            if(crowd){
                ret++;
                i=j+1;
            }else
                i++;
        }

        return ret;

    }
