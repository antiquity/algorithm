    public int maxSum(int a, int b, int c, int d) {
        boolean test=true;
        while(test){
            //System.out.println(a+" "+b+" "+c+" "+d);
            if(a==c && b==d){
                return a+b;
            }
            if(Math.min(a,b) < Math.min(c,d)){
                if(c>d){
                    c-=d;
                }else if(c<d)
                    d-=c;
                else
                    test=false;
            }else if(Math.min(a,b) > Math.min(c,d)){
                if(a>b) a-=b;
                else if(b>a) b-=a;
                else test=false;
            }else if(Math.max(a,b) > Math.max(c,d)){
                if(a>b) a-=b;
                else if(b>a) b-=a;
                else test=false;
            }else{
                if(c>d){ c-=d;
                }else if(c<d) d-=c;
                else test=false;
            }
        }
        return -1;

    }
