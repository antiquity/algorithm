    class Num implements Comparable<Num>{
        int upto,n;
        Num(int upto, int n){
            this.upto=upto;
            this.n=n;
        }
        public int compareTo(Num c){
            return upto-c.upto;
        }
        public String toString(){
            return String.format("[%d, %d]",upto,n);
        }
    }
    public String isFair(int n, int b, int[] upTo, int[] quantity) {
        int q = upTo.length;
        Num[] hints = new Num[q+2];
        for(int i=0; i<q; i++)
            hints[i]=new Num(upTo[i],quantity[i]);
        hints[q]=new Num(0,0);
        hints[q+1]=new Num(b,n);

        Arrays.sort(hints);
        //println(Arrays.toString(hints));
        int minOdd=0, maxOdd=0;
        for(int i=1; i<q+2; i++){
            int dif=hints[i].upto-hints[i-1].upto;
            int difn=hints[i].n-hints[i-1].n;
            if(dif<difn || difn<0 ) return "unfair";
            int nOdd=dif/2;
            if(dif%2==1 && hints[i].upto%2==1) nOdd++;
            minOdd+=Math.max(0,difn-(dif-nOdd));
            maxOdd+=Math.min(difn,nOdd);
            //System.out.format("(%d,%d)",minOdd,maxOdd);
        }
        return (minOdd<=n/2 && n/2<=maxOdd)? "fair" : "unfair";

    }
