    public double calc(int[] parent_, int K) {
        double res=1;
        N=parent_.length+1;

        pO = new double[N];
        pStay = new double[N];
        parent = new int[N-1];
        nChild = new int[N];

        Arrays.fill(pO,0);

        for(int i=0; i<N-1; i++){
            parent[i]=parent_[i];
            nChild[parent[i]]++;
        }

        for(int i=0; i<K; i++){
            Arrays.fill(pStay,0);
            visit(0,1);
            //System.out.format("i=%d:\n",i); System.out.println(Arrays.toString(pO)); System.out.println(Arrays.toString(pStay));
            res=1;
            for(int j=0; j<N; j++)
                pO[j]+=(pStay[j]/res);
            res=0;
            for(int j=0; j<N; j++)
                res+=pStay[j];
        }

        return res;
    }
    void visit(int idx, double prob){
        if(idx==0)
            pStay[idx]=prob*(1-pO[idx]);
        else{
            pStay[idx]=prob*(1-pO[idx]-(1-pO[parent[idx-1]]));
        }
        if(pStay[idx]<prob){
            for(int i=0; i<N-1; i++){
                if(parent[i]==idx){
                    visit(i+1,prob/nChild[idx]);
                }
            }
        }
    }

