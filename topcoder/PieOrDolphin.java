        for(int i:map.keySet()) if(map.get(i)<=0) map.remove(i);
        for(int i:map.keySet()){
            if(map.get(i)<val){
                key=i; val=map.get(i);
            }
        }
        int key2,val2=Integer.MAX_VALUE;
        int idx=0;
        for(int i=0;i<N; i++){
            if((choice1[i]==key || choice2[i]==key) && (res[i]!=0)){
                int j = (choice1[i]==key ? choice2[i] : choice1[i]);
                if(map.get(j)<val2){
                    val2=map.get(j); key2=j;
                    idx=i;
                }
            }
        }

        if(Math.abs(dol[key1]-pie[key1])<Math.abs(dol[key2]-pie[key2])){
            if(dol[key2]>pie[key2]){
                res[idx]=1;
                pie[key2]++;
                dol[key]++;
            }else{
                res[idx]=2;
                pie[key2]++;
                dol[key]++;
            }
            
        }


