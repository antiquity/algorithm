    public int[] correct(String s) {
        int depth=0;
        int N=s.length(), i=0;
        List<Integer> ret=new ArrayList<>();
        for(char x:s.toCharArray()){
            if(x=='('){
                if(depth>=N-i-1){
                    ret.add(i);
                    depth--;
                }else{
                    depth++;
                }
            }else{
                if(depth==0){
                    ret.add(i);
                    depth++;
                }else
                    depth--;
            }
            i++;
        }
        int[] list=new int[ret.size()];
        Iterator<Integer> itr=ret.iterator();
        for(i=0; i<list.length; i++)
            list[i]=itr.next();
        return list;

    }
