    public double calc(int[] hand, int[] sothe) {
        int N=hand.length;
        int known=0;
        int[] larger=new int[N];
        boolean[] missing=new boolean[2*N+1];
        boolean beat=false;
        double ret=0;

        Arrays.sort(sothe);
        Arrays.sort(hand);
        Arrays.fill(larger,0);
        Arrays.fill(missing,true);
        for(int i=0; i<N; i++){
            missing[hand[i]]=false;
            if(sothe[i]!=-1) missing[sothe[i]]=false;
        }

        //println(Arrays.toString(hand));
        //println(Arrays.toString(sothe));

        //println(Arrays.toString(larger));
        for(int i=0; i<N; i++) if(sothe[i]!=-1){
            known++; beat=false;
            for(int j=0; j<N; j++) if(hand[j]>sothe[i] && larger[j]==0){
                larger[j]=1;
                beat=true;
                ret+=1;
                break;
            }
            if(!beat)
                for(int j=0; j<N; j++) if(larger[j]==0){
                    larger[j]=1;
                    break;
                }
        }
        //println(Arrays.toString(larger));
        //println(known);

        int unknown = N-known;
        int count=0;
        for(int j=0; j<N; j++) if(larger[j]==0){
            count=0;
            for(int i=1; i<hand[j]; i++) if(missing[i])
                count++;
            ret+=((double)count)/unknown;
        }

        return ret;
    }
